package kr.or.warehouse.dto;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class MtoVO {

	private String mailNo;
	private int eno;
	private int mtoDel;
	private int mtoCheck;
	private String name;
	private int mtoImpor;

	public int getMtoImpor() {
		return mtoImpor;
	}

	public void setMtoImpor(int mtoImpor) {
		this.mtoImpor = mtoImpor;
	}

	public String getMailNo() {
		return mailNo;
	}

	public void setMailNo(String mailNo) {
		this.mailNo = mailNo;
	}

	public int getEno() {
		return eno;
	}

	public void setEno(int eno) {
		this.eno = eno;
	}

	public int getMtoDel() {
		return mtoDel;
	}

	public void setMtoDel(int mtoDel) {
		this.mtoDel = mtoDel;
	}

	public int getMtoCheck() {
		return mtoCheck;
	}

	public void setMtoCheck(int mtoCheck) {
		this.mtoCheck = mtoCheck;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.JSON_STYLE);
	}
}

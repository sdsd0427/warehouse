package kr.or.warehouse.dto;

import java.util.Date;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class HrTypeVO {
	private String hrTypeCode;
	private String hrType;
	private String onTime;
	private String offTime;
	private int eno;
	private Date startDate;
	private Date endDate;
	private String hrDay;
	private int hrApprove;
	private int weekTime;
	
	private String ipAddress;
	
	public String getIpAddress() {
		return ipAddress;
	}

	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}

	public String getHrTypeCode() {
		return hrTypeCode;
	}

	public void setHrTypeCode(String hrTypeCode) {
		this.hrTypeCode = hrTypeCode;
	}
	public String getHrType() {
		return hrType;
	}

	public void setHrType(String hrType) {
		this.hrType = hrType;
	}

	public String getOnTime() {
		return onTime;
	}

	public void setOnTime(String onTime) {
		this.onTime = onTime;
	}

	public String getOffTime() {
		return offTime;
	}
	public void setOffTime(String offTime) {
		this.offTime = offTime;
	}

	public int getEno() {
		return eno;
	}
	public void setEno(int eno) {
		this.eno = eno;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public String getHrDay() {
		return hrDay;
	}

	public void setHrDay(String hrDay) {
		this.hrDay = hrDay;
	}

	public int getHrApprove() {
		return hrApprove;
	}

	public void setHrApprove(int hrApprove) {
		this.hrApprove = hrApprove;
	}

	public int getWeekTime() {
		return weekTime;
	}

	public void setWeekTime(int weekTime) {
		this.weekTime = weekTime;
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.JSON_STYLE);
	}


}

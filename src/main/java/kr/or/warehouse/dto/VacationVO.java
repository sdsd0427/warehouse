package kr.or.warehouse.dto;


import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class VacationVO {

	private int useVac; // 사용연차
	private int totalVac; // 총연차
	private int remainVac; // 잔여연차
	private int generVac; // 발생연차
	private int eno;

	public int getUseVac() {
		return useVac;
	}

	public void setUseVac(int useVac) {
		this.useVac = useVac;
	}

	public int getTotalVac() {
		return totalVac;
	}

	public void setTotalVac(int totalVac) {
		this.totalVac = totalVac;
	}

	public int getRemainVac() {
		return remainVac;
	}

	public void setRemainVac(int remainVac) {
		this.remainVac = remainVac;
	}

	public int getGenerVac() {
		return generVac;
	}

	public void setGenerVac(int generVac) {
		this.generVac = generVac;
	}

	public int getEno() {
		return eno;
	}

	public void setEno(int eno) {
		this.eno = eno;
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.JSON_STYLE);
	}
}

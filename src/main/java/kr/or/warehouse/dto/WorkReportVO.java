package kr.or.warehouse.dto;

import java.util.Date;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class WorkReportVO {

	private String wcode;
	private int eno;
	private String content;
	private Date regDate;
	private int result;
	private String type;
	private String resContent;
	private Date resDate;
	private String reporter;
	private String reporterPhoto;
	private String reported;
	private String reportedPhoto;
	private int wrepNo;


	public String getResContent() {
		return resContent;
	}
	public void setResContent(String resContent) {
		this.resContent = resContent;
	}
	public Date getResDate() {
		return resDate;
	}
	public void setResDate(Date resDate) {
		this.resDate = resDate;
	}
	public String getReporter() {
		return reporter;
	}
	public void setReporter(String reporter) {
		this.reporter = reporter;
	}
	public String getReporterPhoto() {
		return reporterPhoto;
	}
	public void setReporterPhoto(String reporterPhoto) {
		this.reporterPhoto = reporterPhoto;
	}
	public String getReported() {
		return reported;
	}
	public void setReported(String reported) {
		this.reported = reported;
	}
	public String getReportedPhoto() {
		return reportedPhoto;
	}
	public void setReportedPhoto(String reportedPhoto) {
		this.reportedPhoto = reportedPhoto;
	}
	public int getWrepNo() {
		return wrepNo;
	}
	public void setWrepNo(int wrepNo) {
		this.wrepNo = wrepNo;
	}
	public String getWcode() {
		return wcode;
	}
	public void setWcode(String wcode) {
		this.wcode = wcode;
	}
	public int getEno() {
		return eno;
	}
	public void setEno(int eno) {
		this.eno = eno;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getRegDate() {
		return regDate;
	}
	public void setRegDate(Date regDate) {
		this.regDate = regDate;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.JSON_STYLE);
	}



}
